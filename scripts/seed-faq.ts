async function main() {
  console.log('Seeding FAQ entries...');

  for (const entry of FAQ_ENTRIES) {
    await db.insert(chatFaq).values({
      id: generateId(),
      question: entry.question,
      answer: entry.answer,
      keywords: entry.keywords,
      isActive: true,
      hitCount: 0,
      priority: 0,
    });
    console.log(`  + ${entry.question}`);
  }

  console.log(`\nDone: ${FAQ_ENTRIES.length} FAQ entries seeded.`);
  await client.end();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
